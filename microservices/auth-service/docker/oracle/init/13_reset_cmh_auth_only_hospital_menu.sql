CREATE TABLE CMH.AUTH_ROLE (
    ROLE_CODE VARCHAR2(50) NOT NULL,
    ROLE_NAME VARCHAR2(100) NOT NULL,
    IS_ACTIVE CHAR(1) NOT NULL,
    CREATED_AT DATE NOT NULL,
    UPDATED_AT DATE,
    CONSTRAINT PK_AUTH_ROLE PRIMARY KEY (ROLE_CODE),
    CONSTRAINT CK_AUTH_ROLE_IS_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE TABLE CMH.AUTH_USER (
    ID VARCHAR2(20) NOT NULL,
    USERNAME VARCHAR2(50) NOT NULL,
    PASSWORD_HASH VARCHAR2(100) NOT NULL,
    ROLE_CODE VARCHAR2(50) NOT NULL,
    CONSTRAINT PK_AUTH_USER PRIMARY KEY (ID),
    CONSTRAINT UQ_AUTH_USER_USERNAME UNIQUE (USERNAME),
    CONSTRAINT FK_AUTH_USER_ROLE_CODE FOREIGN KEY (ROLE_CODE) REFERENCES CMH.AUTH_ROLE (ROLE_CODE)
);

CREATE TABLE CMH.MENU (
    MENU_ID NUMBER NOT NULL,
    PARENT_ID NUMBER,
    CODE VARCHAR2(50) NOT NULL,
    NAME VARCHAR2(100) NOT NULL,
    PATH VARCHAR2(200),
    ICON VARCHAR2(50),
    SORT_ORDER NUMBER NOT NULL,
    IS_ACTIVE CHAR(1) NOT NULL,
    CREATED_AT TIMESTAMP(6) NOT NULL,
    UPDATED_AT TIMESTAMP(6) NOT NULL,
    CONSTRAINT PK_CMH_MENU PRIMARY KEY (MENU_ID),
    CONSTRAINT CK_CMH_MENU_IS_ACTIVE CHECK (IS_ACTIVE IN ('Y', 'N'))
);

CREATE TABLE CMH.AUTH_ROLE_MENU_PERMISSION (
    ROLE_CODE VARCHAR2(50) NOT NULL,
    MENU_ID NUMBER NOT NULL,
    CAN_VIEW CHAR(1) NOT NULL,
    CAN_CREATE CHAR(1) NOT NULL,
    CAN_UPDATE CHAR(1) NOT NULL,
    CAN_DELETE CHAR(1) NOT NULL,
    CONSTRAINT PK_AUTH_ROLE_MENU_PERMISSION PRIMARY KEY (ROLE_CODE, MENU_ID),
    CONSTRAINT FK_ARMP_ROLE_CODE FOREIGN KEY (ROLE_CODE) REFERENCES CMH.AUTH_ROLE (ROLE_CODE),
    CONSTRAINT CK_ARMP_CAN_VIEW CHECK (CAN_VIEW IN ('Y', 'N')),
    CONSTRAINT CK_ARMP_CAN_CREATE CHECK (CAN_CREATE IN ('Y', 'N')),
    CONSTRAINT CK_ARMP_CAN_UPDATE CHECK (CAN_UPDATE IN ('Y', 'N')),
    CONSTRAINT CK_ARMP_CAN_DELETE CHECK (CAN_DELETE IN ('Y', 'N'))
);

CREATE TABLE CMH.AUTH_USER_MENU_PERMISSION (
    USER_ID VARCHAR2(20) NOT NULL,
    MENU_ID NUMBER NOT NULL,
    CAN_VIEW CHAR(1),
    CAN_CREATE CHAR(1),
    CAN_UPDATE CHAR(1),
    CAN_DELETE CHAR(1),
    CONSTRAINT PK_AUTH_USER_MENU_PERMISSION PRIMARY KEY (USER_ID, MENU_ID),
    CONSTRAINT FK_AUMP_USER_ID FOREIGN KEY (USER_ID) REFERENCES CMH.AUTH_USER (ID),
    CONSTRAINT FK_AUMP_MENU_ID FOREIGN KEY (MENU_ID) REFERENCES CMH.MENU (MENU_ID),
    CONSTRAINT CK_AUMP_CAN_VIEW CHECK (CAN_VIEW IN ('Y', 'N') OR CAN_VIEW IS NULL),
    CONSTRAINT CK_AUMP_CAN_CREATE CHECK (CAN_CREATE IN ('Y', 'N') OR CAN_CREATE IS NULL),
    CONSTRAINT CK_AUMP_CAN_UPDATE CHECK (CAN_UPDATE IN ('Y', 'N') OR CAN_UPDATE IS NULL),
    CONSTRAINT CK_AUMP_CAN_DELETE CHECK (CAN_DELETE IN ('Y', 'N') OR CAN_DELETE IS NULL)
);

CREATE TABLE CMH.AUTH_SESSION (
    SESSION_ID VARCHAR2(36) NOT NULL,
    USER_ID VARCHAR2(20) NOT NULL,
    ACCESS_TOKEN_JTI VARCHAR2(36),
    REFRESH_TOKEN_JTI VARCHAR2(36),
    LOGIN_AT DATE NOT NULL,
    LAST_ACCESS_AT DATE,
    EXPIRES_AT DATE NOT NULL,
    IS_REVOKED CHAR(1) NOT NULL,
    REVOKED_AT DATE,
    CREATED_AT DATE NOT NULL,
    UPDATED_AT DATE,
    CONSTRAINT PK_AUTH_SESSION PRIMARY KEY (SESSION_ID),
    CONSTRAINT FK_AUTH_SESSION_USER_ID FOREIGN KEY (USER_ID) REFERENCES CMH.AUTH_USER (ID),
    CONSTRAINT CK_AUTH_SESSION_IS_REVOKED CHECK (IS_REVOKED IN ('Y', 'N'))
);

CREATE TABLE CMH.LOGIN_HISTORY (
    LOGIN_HISTORY_ID NUMBER NOT NULL,
    USER_ID VARCHAR2(20),
    USERNAME VARCHAR2(50) NOT NULL,
    LOGIN_RESULT VARCHAR2(20) NOT NULL,
    ATTEMPTED_AT DATE NOT NULL,
    CLIENT_IP VARCHAR2(64),
    FAILURE_REASON VARCHAR2(100),
    CONSTRAINT PK_LOGIN_HISTORY PRIMARY KEY (LOGIN_HISTORY_ID),
    CONSTRAINT FK_LOGIN_HISTORY_USER_ID FOREIGN KEY (USER_ID) REFERENCES CMH.AUTH_USER (ID)
);

