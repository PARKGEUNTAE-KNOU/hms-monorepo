BEGIN
    FOR table_row IN (SELECT table_name FROM user_tables) LOOP
        EXECUTE IMMEDIATE 'DROP TABLE CMH.' || table_row.table_name || ' CASCADE CONSTRAINTS PURGE';
    END LOOP;
END;
/

BEGIN
    FOR sequence_row IN (SELECT sequence_name FROM user_sequences) LOOP
        EXECUTE IMMEDIATE 'DROP SEQUENCE CMH.' || sequence_row.sequence_name;
    END LOOP;
END;
/

BEGIN
    FOR object_row IN (
        SELECT object_name, object_type
        FROM user_objects
        WHERE object_type IN ('FUNCTION', 'TRIGGER')
    ) LOOP
        EXECUTE IMMEDIATE 'DROP ' || object_row.object_type || ' CMH.' || object_row.object_name;
    END LOOP;
END;
