    e.PATIENT_ID,
    'SBAR',
    'Baseline support note linked to encounter ' || e.ID,
    'bootstrap',
    SYSDATE
  FROM CMH.MEDICAL_ENCOUNTER e
  WHERE NOT EXISTS (SELECT 1 FROM CMH.NURSE_SUPPORT_NOTE n WHERE n.ENCOUNTER_ID = e.ID)
    AND e.ROWID IN (SELECT rid FROM (SELECT ROWID rid FROM CMH.MEDICAL_ENCOUNTER) WHERE ROWNUM <= 5);

