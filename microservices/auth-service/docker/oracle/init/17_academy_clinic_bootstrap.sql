  END IF;

  INSERT INTO CMH.NURSE_SUPPORT_NOTE (
    ID, ENCOUNTER_ID, VISIT_ID, PATIENT_ID, SUPPORT_TYPE, NOTE, CREATED_BY, CREATED_AT
  )
  SELECT
    CMH.NURSE_SUPPORT_NOTE_SEQ.NEXTVAL,
    e.ID,
