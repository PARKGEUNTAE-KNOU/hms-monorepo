DECLARE
  FUNCTION table_exists(p_owner IN VARCHAR2, p_table IN VARCHAR2) RETURN NUMBER IS
    v_cnt NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO v_cnt
      FROM ALL_TABLES
     WHERE OWNER = UPPER(p_owner)
       AND TABLE_NAME = UPPER(p_table);
    RETURN v_cnt;
  END;

  FUNCTION seq_exists(p_owner IN VARCHAR2, p_seq IN VARCHAR2) RETURN NUMBER IS
    v_cnt NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO v_cnt
      FROM ALL_SEQUENCES
     WHERE SEQUENCE_OWNER = UPPER(p_owner)
       AND SEQUENCE_NAME = UPPER(p_seq);
    RETURN v_cnt;
  END;

  FUNCTION trigger_exists(p_owner IN VARCHAR2, p_trigger IN VARCHAR2) RETURN NUMBER IS
    v_cnt NUMBER;
  BEGIN
    SELECT COUNT(*)
      INTO v_cnt
      FROM ALL_TRIGGERS
     WHERE OWNER = UPPER(p_owner)
       AND TRIGGER_NAME = UPPER(p_trigger);
    RETURN v_cnt;
  END;

