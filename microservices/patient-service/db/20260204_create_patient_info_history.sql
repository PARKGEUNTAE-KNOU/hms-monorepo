  HISTORY_ID    NUMBER                 NOT NULL,
  PATIENT_ID    NUMBER                 NOT NULL,
  CHANGE_TYPE   VARCHAR2(20 BYTE)      NOT NULL,
  BEFORE_DATA   CLOB                       NULL,
  AFTER_DATA    CLOB                       NULL,
