  TABLESPACE TEAM_DEV_TS
  ENABLE STORAGE IN ROW
  CHUNK 8192
  NOCACHE
  LOGGING
  STORAGE (
    INITIAL     64 K
    NEXT        1 M
    MINEXTENTS  1
    MAXEXTENTS  UNLIMITED
  )
)
